function display_font(data) {
  let i, x = 0, y = 0, ye = y + 3;
  let dx = 24, dp = 4;

  // select 4 pages
  oled128x64.pageAddr(y, ye);

  for (i = 0; i < 10; i++) {
    // select 3 cols
    oled128x64.colAddr(x, x + dx - 1);
    oled128x64.data(data, i * 96, 96);

    x = x + dx;
    if (x >= 128 - dx) {
      x = 0;
      y = y + dp;
      ye = y + 3;
      if (y < 8) {
        oled128x64.pageAddr(y, ye);
      }
    }
    //console.log("x=", x, "y=", y);
  }
}

