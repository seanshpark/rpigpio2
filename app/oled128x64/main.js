class ClockShow {
  static SW = 128;
  static SH = 64;
  static DX = 24;
  static DY = 4;
  static CH = 96; // bytes per character image

  constructor(charbmp) {
    this._charbmp = charbmp;
  }

  // x : 0 ~ 4
  // y : 0 ~ 1
  goto(x, y) {
    if (x < 0 || x > 4) x = 0;
    if (y < 0 || y > 1) y = 0;
    this._x = x * ClockShow.DX;
    this._y = y * ClockShow.DY;

    let ye = this._y + ClockShow.DY - 1;
    oled128x64.colAddr(this._x, this._x + ClockShow.DX - 1);
    oled128x64.pageAddr(this._y, ye);
  }

  print(str) {
    for (const ch of str) {
      let ascii = ch.charCodeAt(0);
      let index = ascii - 32; // ' '
      if (index >= 0 && index < 32) {
        oled128x64.data(this._charbmp, index * ClockShow.CH, ClockShow.CH);
      }
      this._x = this._x + ClockShow.DX;
      if (this._x >= ClockShow.SW - ClockShow.DX) {
        this._x = 0;
        this._y = this._y + ClockShow.DY;
        if (this._y >= 6) {
          this._y = 0;
        }
      }
      oled128x64.colAddr(this._x, this._x + ClockShow.DX - 1);
      let ye = this._y + ClockShow.DY - 1;
      oled128x64.pageAddr(this._y, ye);
    }
  }
}

class ClockTemp {
  constructor(charbmp) {
    this._temperature = "00.00";
    this._timerId = null;
    this._clockshow = new ClockShow(charbmp);
  }

  updateDisplay() {
    this._clockshow.goto(0, 0);
    this._clockshow.print(this._temperature);

    this._clockshow.goto(0, 1);
    this._clockshow.print(this._timenow);

    lcd1602.move(0, 0);
    lcd1602.puts(this._temperature);
    lcd1602.putc(0xdf); // Â°
    lcd1602.puts('C');

    lcd1602.move(1, 0);
    lcd1602.puts(this._timenow);
  }

  readTime() {
    const now = new Date;
    const hh = now.getHours();
    const mm = now.getMinutes();
    let hs = `0${hh}`; hs = hs.substring(hs.length - 2);
    let ms = `0${mm}`; ms = ms.substring(ms.length - 2);

    //const mi = now.getMilliseconds();
    //const blink = mi > 500 ? true : false;
    const se = now.getSeconds();
    const blink = se & 1;

    if (blink)
      this._timenow = `${hs} ${ms}`;
    else
      this._timenow = `${hs}:${ms}`;
  }

  readTemp() {
    let self = this;
    fs.readFile(_temp_file, 'ascii', function(err, data) {
      if (err) return;
      let strcuts = data.split('\n');
      let temperature = parseInt(strcuts[0]) / 1000.0;
      let strTemp = temperature.toPrecision(4);

      self._temperature = strTemp.substring(0, 2) + '.' + strTemp.substring(3, 5);
      self.readTime();
      self.updateDisplay();
    });
  }

  refresh() {
    this.readTemp();
  }

  begin() {
    let self = this;
    this._timerId = setInterval(function() {
      self.refresh();
    }, 500);
  }

  end() {
    if (this._timerId !== null) {
      clearInterval(this._timerId);
      this._timerId = null;
    }
  }
}

